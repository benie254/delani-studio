$(function() {
	$("#project1").css("opacity","1");
	$("#project1").hover(function() {
		$(this).stop().animate({ opacity: 0.8 }, "fast");
	},
	function() {
		$(this).stop().animate({ opacity: 1 }, "fast");
	});
});

